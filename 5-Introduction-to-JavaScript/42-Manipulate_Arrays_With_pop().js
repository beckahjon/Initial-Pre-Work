//I learned you can "pop" a value off the end of an array by using the .pop() function. You can store the popped off value by assigning it to a variable.
// Example
var ourArray = [1,2,3];
var removedFromOurArray = ourArray.pop();
// removedFromOurArray now equals 3, and ourArray now equals [1,2]

