//I learned that parameters are variables that act as placeholders within a function. A function is defined along with one or more parameters. The values that are input, or passed, into a function are called arguments.
// Example
function ourFunctionWithArgs(a, b) {
  console.log(a - b);
}
ourFunctionWithArgs(10, 5); // Outputs 5

// Only change code below this line.
function functionWithArgs(a, b) {
  console.log(a + b);
}
functionWithArgs(13, 45);
