//A "for loop" is the most common type of loop. It runs for a specfic number of times. There are three optional expressions: initialization (executed one time only before the loop starts), condition (continues as long as the condition is true), and final-expression (executed at the end of each loop and can be used to increment or decrement the loop counter).
// Example
var ourArray = [];

for (var i = 0; i < 5; i++) {
  ourArray.push(i);
}

